var i=20 //利润    i<=10w       提10%的奖金
      //        10w< i <20w  低于10w部分提10% 高于10w部分7.5%
      //        20w< i <40w  高于20w部分5%
      //        40W< i <60w  高于40w部分3%
      //        60W< i <100W 高于60w部分1.5%
      //        100< i       高于100w部分1%
var j
if (i<=10) {  //单位w
    j=i*10/100
}else if(i<=20){
    j=(i-10)*7.5/100+10*10/100
}else if(i<=40){
    j=(i-20)*5/100+10*7.5/100+10*10/100
}else if(i<=0){
    j=(i-40)*5/100+20*5/100+10*7.5/100+10*10/100
}else if(i<=100){
    j=(i-60)*1.5/100+20*3/100+20*5/100+10*7.5/100+10*10/100
}else{
    j=(i-100)*1/100+40*1.5/100+20*3/100+20*5/100+10*7.5/100+10*10/100
}
console.log(j);